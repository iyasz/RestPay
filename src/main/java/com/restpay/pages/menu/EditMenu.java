    public void loadProduct(Product product) {
        this.product = product;
        populateForm();
        loadCategories();
    }
    
    private void loadCategories() {
        field_menu_category.removeAllItems();
        categoryList = CategoryRepository.getAll();

        if (categoryList.isEmpty()) {
            field_menu_category.addItem("-- Tidak ada kategori --");
            field_menu_category.setEnabled(false);
            return;
        }

        int selectedIndex = 0;
        field_menu_category.addItem("Select an Option");

        for (int i = 0; i < categoryList.size(); i++) {
            Category cat = categoryList.get(i);
            field_menu_category.addItem(cat.getName());

            // Otomatis pilih kategori produk yang sedang diedit
            if (cat.getId() == product.getCategoryId()) {
                selectedIndex = i + 1; // +1 karena ada "Select an Option" di index 0
            }
        }

        field_menu_category.setSelectedIndex(selectedIndex);
    }
    
    private void populateForm() {
        // Isi semua field dengan data produk yang dipilih
        filed_menu_name.setText(product.getName());
        filed_menu_description.setText(product.getDescription());
        filed_menu_price.setText(String.valueOf(product.getPrice()));
        filed_menu_available.setSelected(product.isAvailable());
    }
    
    private void clearForm() {
        filed_menu_name.setText("");
        filed_menu_description.setText("");
        filed_menu_price.setText("");
        field_menu_category.setSelectedIndex(0);
        filed_menu_available.setSelected(false);
    }

